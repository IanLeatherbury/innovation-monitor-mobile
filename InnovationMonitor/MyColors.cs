using System;
using Xamarin.Forms;

namespace InnovationMonitor
{
    public class MyColors
    {
        public static Color Black
        {
            get { return Color.FromHex("#1B1F23"); }
        }

        public static Color Grey
        {
            get { return Color.FromHex("#D9D9D9"); }
        }

        public static Color White
        {
            get { return Color.FromHex("#FFFFFF"); }
        }

        public static Color LightBlack
        {
            get { return Color.FromHex("#26292E"); }
        }

        public static Color Blue
        {
            get { return Color.FromHex("#4687C8"); }
        }
    }
}
